#include <iostream>
#include <string>

int main() {
    std::string firstName = "Michael";
    std::string lastName  = "Jordan";
    std::string street    = "23 Bulls Street";
    std::string city      = "Chicago, IL";
    std::string zip       = "12345";

    std::cout << "First Name: " << firstName << '\n';
    std::cout << "Last Name: " << lastName << '\n';
    std::cout << "Street Address: " << street << '\n';
    std::cout << "City: " << city << '\n';
    std::cout << "Zip Code: " << zip << '\n';

    return 0;
}
